// Types of JavaScript Console Object Methods

// 1. console.log() - Logs a message to the console.
console.log("Hello, World!");
// 2. console.error() - Logs an error message to the console.
console.error("This is an error message.");
// 3. console.warn() - Logs a warning message to the console.
console.warn("This is a warning message.");
// 4. console.info() - Logs an informational message to the console.
console.info("This is an informational message.");
// 5. console.debug() - Logs a debug message to the console.
console.debug("This is a debug message.");
// 6. console.assert() - Logs a message to the console if the assertion is false.
console.assert(1 === 2, "This is an assertion message.");
// 7. console.clear() - Clears the console.
console.clear();
// 8. console.time() - Starts a timer.
console.time("Timer");
// 9. console.timeEnd() - Stops a timer and logs the elapsed time.
console.timeEnd("Timer");
// 10. console.table() - Logs a table to the console.
console.table([1, 2, 3, 4, 5]);
// 11. console.count() - Logs the number of times a message has been logged.
console.count("Count");
// 12. console.group() - Starts a group.
console.group("Group");
// 13. console.groupEnd() - Ends a group.
console.groupEnd("Group");
// 14. console.trace() - Logs a stack trace to the console.
console.trace("Trace");
// 15. console.dir() - Logs an object to the console.
console.dir({ name: "John", age: 30 });
// 16. console.dirxml() - Logs an XML object to the console.
console.dirxml(document.body);
// 17. console.profile() - Starts a JavaScript CPU profile.
console.profile("Profile");
// 18. console.profileEnd() - Ends a JavaScript CPU profile.
console.profileEnd("Profile");
// 19. console.timeStamp() - Adds a marker to the browser's Timeline or Waterfall tool.
console.timeStamp("TimeStamp");
// 20. console.memory() - Logs memory usage to the console.
console.memory();
// 21. console.exception() - Logs an exception to the console.
console.exception("This is an exception message.");
// 22. console.groupCollapsed() - Starts a collapsed group.
console.groupCollapsed("GroupCollapsed");
// 23. console.groupEnd() - Ends a collapsed group.
console.groupEnd("GroupCollapsed");
// 24. console.markTimeline() - Adds a marker to the browser's Timeline or Waterfall tool.
console.markTimeline("MarkTimeline");
// 25. console.timeline() - Starts a timeline.
console.timeline("Timeline");
// 26. console.timelineEnd() - Ends a timeline.
console.timelineEnd("Timeline");
// 27. console.timeLog() - Logs the current value of a timer.
console.timeLog("Timer");